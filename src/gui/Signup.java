    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            String name,username,password,confirmpassword;
            name = nameus.getText();
            username = username1.getText();
            password = String.valueOf(passw.getPassword());
            confirmpassword = String.valueOf(cpassw.getPassword());

            if(!password.equals(confirmpassword)){
                JOptionPane.showMessageDialog(null, "Password does not match!");
            }
            else{
                if (!name.equals("") && !username.equals("") && !password.equals("")) {
                    database.dbclass.st.executeUpdate("Insert into user(username,password,Name) values('"+username+"','"+password+"','"+name+"') ");
                    JOptionPane.showMessageDialog(null, "User created");
                    this.dispose();
                    loginFrameOpened = true; // Atur status frame Login
                    new Login().setVisible(true);
                } else {
                    System.out.println("error");
                }            }
        }catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Username already exist!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

